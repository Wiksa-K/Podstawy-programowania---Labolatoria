print("1. ",type(1 + 2)) # "+" -  Operator dodawania
print("2. ",type(1 + 4.5)) # "+" - Operator dodawania
print("3. ",type(3/2)) # "/" - Operator dzielenia (z resztą)
print("4. ",type(4/2)) # "/" - Operator dzielenia (z resztą)
print("5. ",type(3//2)) # "/" Operator dzielenia całkowitego (bez reszty)
print("6. ",type(-3//2)) # "/" - Operator dzielenia całkowitego (bez reszty)
print("7. ",type(11 % 2)) # "%" - Operator modulo (wypisanie tylko reszty z dzielenia podanych liczb)
print("8. ",type(2 ** 10)) # "**" - Operator potęgowania
print("9. ",type(8 ** (1/3))) # "**" -  Operator potęgowania, "/" - Operator potęgowania (z resztą), "()" - służy do grupowanina wyrażeń i określania kolejności działań