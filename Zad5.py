a = float(input("Podaj długość boku a: ")) # Funkcja "float()" pozwala operować na podanych liczbach (dodawanie/mnożenie) oraz sprawia, że będą zmiennoprzecinkowe
b = float(input("Podaj długość boku b: ")) # Funkcja "float()" pozwala operować na podanych liczbach (dodawanie/mnożenie) oraz sprawia, że będą zmiennoprzecinkowe

pole = a * b # Przypisanie do zmiennej "pole" wyniku równania "a * b"
obwod = (2 * a) + (2 * b) # Przypisanie do zmiennej "pole" wyniku równania "(2 * a) + (2 * b)"

print("Pole prostokąta wynosi",pole ,"cm kwadratowych, obwód wynosi",obwod,"cm") # Napisanie wyniku z użyciem powyższych zmiennych