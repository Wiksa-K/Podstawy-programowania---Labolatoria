cena = 39.99 # Przypisanie do zmiennej "cena" liczby "39.99"
rabat = 0.2 # Przypisanie do zmiennej "rabat" liczby "0.2"
nowa_cena = cena * (1 - rabat) # Przypisanie do zmiennej "rabat" nowej ceny

print("Cena po obniżce to",round(nowa_cena, 2)) # Funkcja "round(x,2)" zaokragla podaną wartość x do 2 miejsc po przecinku